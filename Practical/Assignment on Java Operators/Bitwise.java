// Demo bitwise operators with 3 values..

class Bitwise
{
public static void main(String[] args)
{
int a,b,c;
a=10;
b=5;
c=2;
System.out.println(a&b&c);
System.out.println(a|b|c);
System.out.println(a^b^c);
System.out.println(a<<b<<c);
System.out.println(a>>b>>c);
}
}